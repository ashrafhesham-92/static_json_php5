	/**
	 * Update the specified resource in storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function update(Request $request, $id)
	{
		//
		$row = \App\row::find($id);

		$action = \App\action::find($request->action);



		$row->actions()->save($action);


		return back()->with('success','cell has been updated');
	}

	/**
	 * Remove the specified resource from storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function destroy($id, Request $request)
	{
		$row = \App\row::find($id);

		//to remove excessive cells in order to prevent cells table from having unused data in the future
		
		$row->delete();

		return back()->with('success','row has been removed');
	}

